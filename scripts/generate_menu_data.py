BASE_PRICES = {"밥": 9500, "면": 9000, "육류": 12500, "기타": 9000}


def contains_any(name, words):
    return any(word in name for word in words)


def tags_for(name, medium):
    if name in TAG_OVERRIDES:
        return TAG_OVERRIDES[name]
    tags = []
    if name not in VEGETARIAN_NAMES:
        for tag, words in MEAT_KEYWORDS.items():
            if contains_any(name, words):
                tags.append(tag)
        if tags:
            tags.append("육류전체")
    if contains_any(name, SEAFOOD):
        tags.append("해산물")
    if contains_any(name, FISH):
        tags.append("생선")
    if contains_any(name, SHELLFISH):
        tags.append("갑각류")
    if contains_any(name, SPICY):
        tags.append("매운맛")
    if contains_any(name, WHEAT) or medium == "면":
        tags.append("밀가루")
    if contains_any(name, DAIRY):
        tags.append("유제품")
    if "버섯" in name or "양송이" in name:
        tags.append("버섯")
    if "고수" in name or name in ["쌀국수", "분짜", "반미", "타코", "부리또"]:
        tags.append("고수")
    return list(dict.fromkeys(tags))


def price_for(large, medium, index, tags):
    name = CURRENT_NAME
    if name in PRICE_OVERRIDES:
        return PRICE_OVERRIDES[name]
    price = BASE_PRICES[medium] + (index % 7) * 500
    if medium == "육류":
        price += 2000
    if medium == "기타":
        price += 500
    if large == "양식":
        price += 1500
    if large == "일식":
        price += 1000
    if large == "기타":
        price += 500
    if "해산물" in tags or "갑각류" in tags:
        price += 1000
    return round(price / 500) * 500


